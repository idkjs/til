#!/usr/bin/env python3

a = "String"
b = "Hello"
n = 3
m = 5
s = 2

print(ord(a[0]))  # aの一文字目(S)をアスキーコードで出力
print(chr(65))    # アスキーコードから文字へ

print(a + b)     # 文字列 a と 文字列 b を連結します
print(a * n)     # 文字列 a を n 回繰り返します
print(a[n])      # 文字列 a の中の n 番目の文字を取り出します
print(a[n:m])    # 文字列 a の中の n 番目から m 番目までの文字列を取り出します
print(a[n:])     # 文字列 a の中の n 番目から最後までの文字列を取り出します
print(a[:m])     # 文字列 a の中の 0 番目から m 番目までの文字列を取り出します
print(a[n:m:s])  # 文字列 a の中の n 番目から m 番目までの文字列を s個とばしで取り出します<Paste>
